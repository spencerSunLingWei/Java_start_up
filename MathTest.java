package com.stage1.part3.task1;

public class MathTest {

    public static void main(String[] args) {

        System.out.println("max between two number: " + Math.max(10,20));
        System.out.println("min between two number: " + Math.min(10,20));

        System.out.println("power: " + Math.pow(10,3));
        System.out.println("sqrt: " + Math.sqrt(100));

        System.out.println("absolute value: " + Math.abs(-3.14));
        System.out.println("rounded to: " + Math.round(9.99));
        System.out.println("random number: " + Math.random());


    }
}
